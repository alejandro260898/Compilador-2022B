    PATRON_OP_MULTIPLICACION = "^[*\/]$"
    PATRON_OP_ASIGNACION = "^=$"
    PATRON_OP_RELACIONAL = "^[<>]=?$"
    PATRON_OP_IGUALDAD = "^[!=]=$"
    PATRON_OP_AND = "^&&$"
    PATRON_OP_OR = "^\|\|$"
    PATRON_OP_NOT = "^!$"
    PATRON_PARENTESIS_INICIO = "^\($"
    PATRON_PARENTESIS_FIN = "^\)$"
    PATRON_LLAVES_INICIO = "^\{$"
    PATRON_LLAVES_FIN = "^\}$"
    PATRON_COMA = "^,$"
    PATRON_PUNTO_COMA = "^;$"
    PATRON_PESOS = "^\$$"
    PATRON_TIPO = "^(void|int|float|char|string|boolean)$"
    PATRON_IF = "^if$"
    PATRON_ELSE = "^else$"
    PATRON_WHILE = "^while$"
    PATRON_RETURN = "^return$"